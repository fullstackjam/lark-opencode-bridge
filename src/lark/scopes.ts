export function formatScopesJson(manifest: ScopeManifest = DEFAULT_SETUP_SCOPES): string {
  return JSON.stringify(manifest, null, 2);
}

export function permissionsConsoleUrl(appId: string, brand: "feishu" | "lark" = "feishu"): string {
  const host = brand === "lark" ? "open.larksuite.com" : "open.feishu.cn";
  return `https://${host}/app/${appId}/auth`;
}
